def my_calc():
    hh = float(input('Введите количество оработанных часов: '))
    sum_h = float(input('Введите сумму оплаты труда в час: '))
    prize = float(input('Введите размер премии: '))
    result = hh * sum_h
    return result + prize
print(f' Размер заработной платы составил: {my_calc() }')

